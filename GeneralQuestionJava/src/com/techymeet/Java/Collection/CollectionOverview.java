package com.techymeet.Java.Collection;

public class CollectionOverview {

	public static void main(String[] args) {

		// 1 )ArrayList
		// Java ArrayList class uses a dynamic array for storing the elements.
		// It is like an array, but there is no size limit.
		// Java ArrayList class can contain duplicate elements.
		// Java ArrayList class maintains insertion order.
		// Java ArrayList class is non synchronized.
		// Java ArrayList allows random access because the array works on an index
		// basis.

		// 2 )LinkedList class
		// LinkedList class hierarchy
		// LinkedList class uses a doubly linked list to store the elements.
		// LinkedList class can contain duplicate elements.
		// LinkedList class maintains NOT insertion order.

		// 3 )Queue Interface
		// The interface Queue is available in the java.util package and does extend the
		// Collection interface. It is used to keep the elements that are processed in
		// the First In First Out (FIFO)
		// Queue class can contain duplicate elements.
		// As discussed earlier, FIFO concept is used for insertion and deletion of
		// elements from a queue.

		// 4 )Deque& ArrayDeque
		// We know that the stack supports the Last In First Out (LIFO) operation,
		// Deque class can contain duplicate elements.
	}
}
